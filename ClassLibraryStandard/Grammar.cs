        public static bool IsVerb(string word)
        {
            word = word.ToLower();

            if(RegularVerbs.Contains(word)
                || IrregularVerbsBase.Contains(word)
                || IrregularVerbsPastParticiple.Contains(word)
                || IrregularVerbsPastSimple.Contains(word))
            {
                return true;
            }

            foreach(var verb in RegularVerbs)
            {
                if (word == $"{verb}ed" || word == $"{verb}ing")
                    return true;
            }

            return false;
        }

